# 练习1：创建一个变量，存储你的名字，然后输出
your_name = "你的名字"
print(your_name)

# 练习2：计算圆的面积（半径为5）
# 公式：面积 = π × 半径²
radius = 5
pi = 3.14
area = pi * radius * radius
print(f"半径为{radius}的圆的面积是：{area}")

# 练习3：计算矩形的周长（长10，宽5）
length = 10
width = 5
perimeter = 2 * (length + width)
print(f"长为{length}，宽为{width}的矩形周长是：{perimeter}")
