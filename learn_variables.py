# ============================================
# Python变量学习指南 - 第一课
# ============================================

# 什么是变量？
# 变量就是给数据起个名字，方便后面使用
# 就像给东西贴标签一样

# ============================================
# 1. 创建变量（基础）
# ============================================

# 创建一个数字变量
age = 18
#    ↑     ↑
#  变量名  值

# 创建一个文字变量（字符串）
name = "小明"
#    ↑     ↑
# 变量名  值（注意：文字需要用引号）

# ============================================
# 2. 输出变量
# ============================================

print(age)    # 输出：18
print(name)   # 输出：小明

# ============================================
# 3. 变量计算
# ============================================

# 创建两个变量
a = 10
b = 3

# 加法
sum_result = a + b  # 10 + 3 = 13
print("和：" + str(sum_result))

# 减法
sub_result = a - b  # 10 - 3 = 7
print("差：" + str(sub_result))

# 乘法
mul_result = a * b  # 10 * 3 = 30
print("积：" + str(mul_result))

# 除法
div_result = a / b  # 10 / 3 = 3.333...
print("商：" + str(div_result))

# ============================================
# 4. 连接字符串
# ============================================

# 方法1：用 + 号连接
message = name + "今年" + str(age) + "岁"
print(message)  # 输出：小明今年18岁

# 方法2：用f-string（更简单）
message2 = f"{name}今年{age}岁"
print(message2)  # 输出：小明今年18岁

# ============================================
# 5. 实际应用：购物计算
# ============================================

# 定义商品价格
apple_price = 5
orange_price = 3

# 定义购买数量
apple_count = 2
orange_count = 3

# 计算总价
apple_total = apple_price * apple_count    # 5 * 2 = 10
orange_total = orange_price * orange_count # 3 * 3 = 9
total = apple_total + orange_total         # 10 + 9 = 19

# 输出结果
print(f"2个苹果的价格：{apple_total}元")
print(f"3个橘子的价格：{orange_total}元")
print(f"总共要花：{total}元")

# ============================================
# 6. 变量交换（高级技巧）
# ============================================

x = 10
y = 20

# 交换变量的值
x, y = y, x

print(f"交换后 x: {x}")  # 输出：20
print(f"交换后 y: {y}")  # 输出：10

# ============================================
# 7. 变量命名规则
# ============================================

# ✅ 合法的变量名
my_name = "张三"
user_age = 25
_private_var = "私有变量"

# ❌ 不合法的变量名
# 123abc = 1      # 不能以数字开头
# my-name = "李四" # 不能包含连字符
# if = 10         # 不能使用关键字

# ============================================
# 8. 变量类型
# ============================================

# 整数（没有小数点）
integer_var = 42

# 浮点数（有小数点）
float_var = 3.14

# 字符串（文字）
string_var = "Hello"

# 布尔值（真或假）
bool_var = True

# 查看变量类型
print(type(integer_var))  # <class 'int'>
print(type(float_var))    # <class 'float'>
print(type(string_var))   # <class 'str'>
print(type(bool_var))     # <class 'bool'>

